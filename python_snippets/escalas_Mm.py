# A escala maior é compreendida como tendo forma única:
escala_maior=[0,2,4,5,7,9,11]
# Já a escala menor tem 3 formas:
escala_menor_natural=[0,2,3,5,7,8,10]
escala_menor_hamonica=[0,2,3,5,7,8,11]
# além disso, a escala melódica tem 
# a forma ascendente distinta da descendente:
escala_menor_melodica_ascendente=[0,2,3,5,7,9,11]
escala_menor_melodica_descendente=[10,8,7,5,3,2,0]
